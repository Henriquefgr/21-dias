// switch case
let opcao = prompt('escolha 1 de 5 opcoes')

switch(opcao){
    case '1':
        console.log('voce escolheu opcao 1 ')
        break
    case '2':
        console.log('voce escolheu a opcao 2')
        break
    case '3':
        console.log('voce escolheu a opcao 3')
        break
    case '4':
        console.log('voce escolheu a opcao 4')
        break
    case '5':
        console.log('voce escolheu a opcao 5')
        break
    default:
        console.log('erro')
        break    
}